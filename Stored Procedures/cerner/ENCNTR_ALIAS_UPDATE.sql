BEGIN
	CREATE TABLE #encntr_alias_update (
		ENCNTR_ALIAS_ID INT
		,ENCNTR_ID INT
		,UPDT_CNT INT
		,UPDT_DT_TM DATETIME2
		,UPDT_ID INT
		,UPDT_TASK INT
		,UPDT_APPLCTX INT
		,ACTIVE_IND INT
		,ACTIVE_STATUS_CD INT
		,ACTIVE_STATUS_DT_TM DATETIME2
		,ACTIVE_STATUS_PRSNL_ID INT
		,ALIAS_POOL_CD INT
		,ENCNTR_ALIAS_TYPE_CD INT
		,ALIAS VARCHAR(200)
		,ENCNTR_ALIAS_SUB_TYPE_CD INT
		,CHECK_DIGIT INT
		,CHECK_DIGIT_METHOD_CD INT
		,BEG_EFFECTIVE_DT_TM DATETIME2
		,END_EFFECTIVE_DT_TM DATETIME2
		,DATA_STATUS_CD INT
		,DATA_STATUS_DT_TM DATETIME2
		,DATA_STATUS_PRSNL_ID INT
		,CONTRIBUTOR_SYSTEM_CD INT
		,ASSIGN_AUTHORITY_SYS_CD INT
		,ENTERPRISE_UPDATE_DT_TM DATETIME2
	)
	
	DECLARE @StartDate datetime2
	DECLARE @EndDate datetime2
	SET @StartDate = dateadd(day,-2,convert(date,getdate()))
	SET @EndDate = convert(date,getdate())

	INSERT INTO #encntr_alias_update
	EXEC('
	select
		ENCNTR_ALIAS_ID
		,ENCNTR_ID
		,UPDT_CNT
		,UPDT_DT_TM
		,UPDT_ID
		,UPDT_TASK
